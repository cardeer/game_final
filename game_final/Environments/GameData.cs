        public static void SetBallTemplate(int x, int y, int ballTypeCode)
        {
            int newX = x * 2;
            if (y % 2 == 1 && newX % 2 == 0) {
                //Debug.WriteLine("expected odd, got even");
                //Debug.WriteLine($"{y} {newX}");

                newX -= 1;

                if (newX <= 0) newX = 1;

                if (BallsTemplate[y, newX] == 1) newX += 2;
            }

            BallsTemplate[y, newX] = ballTypeCode;
            CanShoot = true;
        }
